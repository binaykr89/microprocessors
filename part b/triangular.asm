.MODEL SMALL
.STACK 5000
.DATA
MESSAGE2 DB 'PROGRAM IS RUNNING.OBSERVE THE O/P WAVE FORM(TRIANGULAR) ON CRO.'13,10,'$'
SHOW DB 'SHOW',13,10,'$'

CR EQU ODOA3H
PA EQU ODOA0H
PB EQU ODOA1H
PC EQU ODOA2H

.CODE
MOV AX,@DATA
MOV DS,AX
MOV AH,09H
MOV DX,OFFSET MESSAGE2
INT 21H

AGAIN:
MOV AL,80H
MOV DX,CR
OUT DX,AL
MOV AL,00H
LOOP1:
MOV DX,PA
OUT DX,AL
MOV DX,PB
OUT DX,AL
MOV BX,0FFFFH
DELA:
DEC BX
JNZ DELA
INC AL
CMP AL,0FFH
JNZ LOOP1
MOV AL,0FFH
LOOP2:
MOV DX,PA
OUT DX AL
MOV DX,PB
OUT DX,AL
MOV BX,0FFFFH
DELA1:DEC BX
JNZ DELA1
DEC AL
CMP AL,00H
JNZ LOOP2
JMP AGAIN
MOV AH,04CH
INT 21H

END